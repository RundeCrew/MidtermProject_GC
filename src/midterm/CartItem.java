package midterm;

public class CartItem {
	private int quantity;

	public CartItem(int quantity) {
		super();
		this.quantity = quantity;
	}

	public CartItem() {
		super();
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	
}
